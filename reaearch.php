	<h5><u>Standardization of plant growth regulators for successful tissue culture of sweet potato</u></h5>
	<h6>-by Horticulture depertment pstu</h6><br>
	<p>The experiment was carried out during the period from June 2016 to January, 2017 at the Plant Biotechnology Laboratory, Department of Horticulture, Patuakhali Science and Technology University (PSTU) for in vitro regeneration of sweet potato. In this study, the nodal segment explants and different combinations of growth regulators were used for in vitro regeneration of sweet potato. BAP (6-benzylamino purine) and KIN (Kinetin) were used for in vitro shoot regeneration and IBA (Indole-6- Butyric Acid) with NAA (Naphthalene Acetic Acid) were used for in vitro root regeneration. The highest percentage of shoot initiation (91.30), the minimum number of days (9.00) for shoot initiation, the highest number of shoot/plantlet (11.00) and the highest shoot length (4.38 cm) was observed when nodal segment explants cultured on MS medium supplemented with BAP 1.5 mg/L + KIN 0.1 mg/L. On the other hand, the highest root initiation percentage (94.12), the minimum number of days (6.00) for root initiation, the highest number of root/plantlet (9.33) and the highest root length (11.13 cm) was observed in the MS medium supplemented with IBA 0.5 mg/L + NAA 0.1 mg/L. Regenerated plantlets were acclimatized for 5 days and adopted in soil by 6-7 days of transplantation. J. Bangladesh Agril. Univ. 16(2): 178-181, August 2018</p>
	<h5><u>The Usefullness Of Cell Phones For Crop Farmers In Selected Regions Of Bangladesh</u></h5>
	<h6>-by the Department of Agricultural Extension and Rural Development</h6><br>
	<p>This research endeavours the usefulness of cell phones for crop farmers in selected region of Bangladesh. For adequate findings and to achieve its purpose, structured interview schedule was adopted to collect data from 281 randomly selected farmers and it was revealed that a little over 60% of them found cell phones very useful, while only 5.3% respondents found the cell phone as less useful. Based on average talk time hours spend in the last six months, top three sources of agricultural information were friends and relatives, distributors and middlemen, and farmers in advanced categories. The results of the ordered logit model showed that their usefulness was significantly determined by age, farm size, per month call charges, and experience in using cell phones. Higher call rates, lack of awareness and paucity of mobile-based information sources were major bottlenecks in using cell phones for agricultural information. The recommendations suggested therein lead to connecting farmers with reliable and rich information sources, use of MMS and SMS, voice call activities, providing subsidized SIM cards, and ultimately undertake widespread campaigns for training of aged farmers to persuade their interest towards the use of cell phones and mobile-based information sources. Contribution/ Originality To the best of authors' knowledge other researches did not consider the types of services provided to farmers for receiving sufficient agricultural information. Moreover, this study is quite in contrast with other researches undertaken earlier on the use of cell-phones in Bangladesh, since it considers the concrete and appropriate usefulness, and also able to provide a holistic scenario of use of cell-phones along with its extended and vast informative benefits to the farmers.</p>


